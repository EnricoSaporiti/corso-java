package it.corso.java;


/**
 * 
 * @author Achille
 *
 *	<p>La classe implementa i metodi di calcolo algebrico</p>
 *	
 * 
 */
public class Calcolatrice {
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return ritorma la somma di a+b
	 */
	
	public double somma(double a, double b) {
		return a + b;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return ritorna la differenza tra a e b
 	 */
	public double sottrazione(double a, double b) {
		return a - b;
	}

	public double moltiplicazione(double a, double b) {
		return a * b;
	}

	public double divisione(double a, double b) {
		return a / b;
	}

	
	public int somma(int a, int b) {
		return a + b;
	}

	public int sottrazione(int a, int b) {
		return a - b;
	}

	public int moltiplicazione(int a, int b) {
		return a * b;
	}

	public int divisione(int a, int b) {
		return a / b;
	}

	
	
	
}
